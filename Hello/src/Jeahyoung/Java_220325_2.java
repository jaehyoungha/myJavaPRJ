package Jeahyoung;

public class Java_220325_2 extends Java_220325_1 { 
public static void main (String [] args) {
    System.out.printf("%d\n", 123);// 정수 출력
    System.out.printf("%5d\n", 123);// 정수 5자리 확보
    System.out.printf("%05d\n", 123);// 정수 5자리 확보, 공백 0으로 출
    
    System.out.printf("%f \n", 123.45);//실수 출력 
    System.out.printf("%7.1f\n", 123.45);//실수 출력 7자리확보,소수점1의 자
    System.out.printf("%7.3f\n", 123.45);//실수 출력 자리확보,소수점 셋째자
    
    System.out.printf("%s\n", "Hi~Java"); //문자열 출력
    System.out.printf("%10s\n", "Hi~Java");//문자열 출력 10자리 확보
    
    
    
    
}

}
